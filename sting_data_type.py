"""
문자열 : 키보드로 쓸 수 있는 문자들을 표현하는 자료형
"""

print("코드잇")
print("유재석")

# 문자안에 포함되는 ', " 표시하기
# print('I'm excited to learn Python!') -> 오류
print("I'm excited to learn Python!") # 작은따옴표를 큰따옴표로 변경
print("I\'m \"excited\" to learn Python!") # 문자로 표시할 따옴표 앞에 \추가

# 문자연결 + *
print("Hello" + "World")
print("Hello" * 3) # 뒤의 숫자만큼 반복
print("3" + "5") # 35로 표시

